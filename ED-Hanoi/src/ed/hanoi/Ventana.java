    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        // TODO add your handling code here:

        try {
            hanoi = new Hanoi();
            int num_discos = Integer.parseInt(this.txt_numDiscos.getText());
            String torre_origen = this.txt_torreOrigen.getText();
            String torre_auxiliar = this.txt_torreAuxiliar.getText();
            String torre_destino = this.txt_torreDestino.getText();
            this.txa_imprimir.setText(hanoi.HANOI(torre_origen, torre_auxiliar, torre_destino, num_discos));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "complete correctamente todos los campos por favor");
            this.txt_numDiscos.setText("0");
            this.txt_torreAuxiliar.setText("");
            this.txt_torreDestino.setText("");
            this.txt_torreOrigen.setText("");
            this.txa_imprimir.setText("");
        }
    }//GEN-LAST:event_btn_aceptarActionPerformed

