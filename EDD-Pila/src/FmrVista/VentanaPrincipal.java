    private void btn_reporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_reporteActionPerformed
        Object auxObject;
        Notas auxNotas;
        Pila auxPila = new Pila(this.pila.getTamaño());
        String informacion = "";

        if (this.pila != null) {
            while (this.pila.vacio() == false){                            
                auxObject = this.pila.cima();
                auxNotas = (Notas) auxObject;
                auxPila.apilar(auxNotas);
                informacion += "\n" + "Cédula: " + auxNotas.getCédula() +
                        " Código materia: " + auxNotas.getCodigoMateria() +
                        " Nota 1: " + auxNotas.getNota1() + 
                        " Nota 2: " + auxNotas.getNota2() +
                        " Nota Supletorio: " + auxNotas.getNotaSupletorio();
                this.pila.desapilar();
            }
            while (auxPila.vacio() == false){
                this.pila.apilar(auxPila);
                auxPila.desapilar();
                
            }
            this.txa_imprimir.setText(informacion);
        } else {
            this.texto += "\nla pila aun no ha sido creada";
            this.txa_imprimir.setText(texto);
        }
    }//GEN-LAST:event_btn_reporteActionPerformed

