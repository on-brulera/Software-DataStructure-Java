    private void btn_crearPilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_crearPilaActionPerformed
        int tamaño;
        if (this.validarNumero(this.txt_tamañoPila.getText())) {
            tamaño = Integer.parseInt(this.txt_tamañoPila.getText());
            if (tamaño > 0) {
                this.pila = new Pila(tamaño);
                this.texto += "\npila creada correctamente";
                this.txa_imprimir.setText(texto);
            } else {
                this.texto += "\nla pila no pudo ser creada";
                this.txa_imprimir.setText(texto);
            }
        }
    }//GEN-LAST:event_btn_crearPilaActionPerformed

    public boolean validarNumero(String dato) {
        try {
            Integer.parseInt(dato);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

    private void btn_apilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_apilarActionPerformed

        if (this.pila == null) {
            this.texto += "\n" + "la pila no ha sido creada";
            this.txa_imprimir.setText(texto);
        } else if (!this.pila.lleno()) {
            String cedula = this.txt_numCedula.getText();
            String codMateria = this.txt_codMateria.getText();
            double n1 = Double.parseDouble(this.txt_nota1.getText());
            double n2 = Double.parseDouble(this.txt_nota2.getText());
            double nSupletorio = Double.parseDouble(this.txt_notaSupletorio.getText());

            Notas notasDatos = new Notas(cedula, codMateria, n1, n2, nSupletorio);
            this.texto += "\n" + this.pila.apilar(notasDatos);
            this.txa_imprimir.setText(texto);
        } else {
            this.texto += "\nno se pudo apilar porque la pila esta llena";
            this.txa_imprimir.setText(texto);
        }
    }//GEN-LAST:event_btn_apilarActionPerformed

    private void btn_desapilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_desapilarActionPerformed
        if (this.pila != null) {
            if (!this.pila.vacio()) {
                this.texto += "\n" + this.pila.desapilar();
                this.txa_imprimir.setText(texto);
            } else {
                this.texto += "\n" + "No se puede desapilar la pila porque esta vacia";
                this.txa_imprimir.setText(texto);
            }
        } else {
            this.texto += "\n" + "No se pudo desapilar por que aun no se ha creado la pila";
            this.txa_imprimir.setText(texto);
        }

    }//GEN-LAST:event_btn_desapilarActionPerformed

