    private void btn_conjuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_conjuntoActionPerformed

        String[] conjunto = this.txt_conjunto.getText().replaceAll("\\s", "").split(",");
        if (this.VALIDAR_CONJUNTO(conjunto)) {
            this.aux_tb = new DefaultTableModel();
            this.aux_tb.setColumnCount(conjunto.length);
            this.aux_tb.setRowCount(conjunto.length);
            this.tb_A.setModel(aux_tb);
        } else {
            this.txt_conjunto.setText("");
            JOptionPane.showMessageDialog(null, "El conjunto solo acepta números");
        }
    }//GEN-LAST:event_btn_conjuntoActionPerformed

