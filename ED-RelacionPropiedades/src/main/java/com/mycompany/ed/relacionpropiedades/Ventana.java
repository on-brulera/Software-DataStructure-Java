    private void btn_relacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_relacionActionPerformed
        if (this.txt_conjunto.getText().replaceAll("\\s", "").equals("")) {
            JOptionPane.showMessageDialog(null, "Escriba un conjunto de números por favor");
        } else {
            String[] conjunto = this.txt_conjunto.getText().replaceAll("\\s", "").split(",");
            int tamaño = conjunto.length;
            int[][] matriz = new int[tamaño][tamaño];
            for (int i = 0; i < tamaño; i++) {
                for (int j = 0; j < tamaño; j++) {
                    matriz[i][j] = Integer.parseInt(this.tb_A.getValueAt(i, j) + "");
                }
            }
            if (this.relacion == null) {
                this.relacion = new Relacion(matriz);
            } else {
                this.relacion.setMatriz(matriz);
            }
        }
    }//GEN-LAST:event_btn_relacionActionPerformed

