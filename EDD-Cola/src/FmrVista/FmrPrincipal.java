    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed
        try {
            int verificador = this.cola.getNumElementos();
            String codigoProducto = txt_codigo.getText();
            String nombre = txt_nombre.getText();
            int cantidad = Integer.parseInt(txt_cantidad.getText());
            double precio = Double.parseDouble(txt_precio.getText());
            int minimo = Integer.parseInt(txt_minimo.getText());
            this.producto = new Productos(codigoProducto, nombre, cantidad, precio, minimo);
            this.cola.ingresar(this.producto);
            if (verificador != this.cola.getNumElementos()) {
                this.texto = "\n dato ingresado correctamente";
            } else {
                this.texto = "\n dato no ingresado cola llena";
            }
            this.txa_imprimir.setText(texto);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "La cola aun no se ha creado o los datos ingresados fueron incorrectos");
        }
    }//GEN-LAST:event_btn_ingresarActionPerformed

