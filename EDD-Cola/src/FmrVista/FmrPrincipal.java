    private void btn_comprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_comprarActionPerformed
        try {
            String dato = "";
            String codigo = this.txt_codigoCV.getText();
            int cantidad = Integer.parseInt(this.txt_cantidadCV.getText());
            Object objectAux;
            Cola colaAux = new Cola(this.cola.getTamaño());
            Productos productoAux;
            while (!this.cola.vacio()) {
                objectAux = this.cola.retirar();
                productoAux = (Productos) objectAux;
                if (productoAux.getCodigoProducto().equals(codigo)) {
                    if (cantidad > 0) {
                        productoAux.setCantidad(productoAux.getCantidad() + cantidad);
                        dato = "Compra realizada correctamente";
                    } else {
                        dato = "Compra no realizada, pedido mal realizado";
                    }
                }
                colaAux.ingresar(productoAux);
            }
            this.cola = colaAux;
            this.txa_imprimir.setText(dato);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "La cola aun no se ha creado o el pedido realizado es incorrecto");
        }
    }//GEN-LAST:event_btn_comprarActionPerformed

    private void btn_venderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_venderActionPerformed
        try {
            String dato = "";
            String codigo = this.txt_codigoCV.getText();
            int cantidad = Integer.parseInt(this.txt_cantidadCV.getText());
            Object objectAux;
            Cola colaAux = new Cola(this.cola.getTamaño());
            Productos productoAux;
            while (!this.cola.vacio()) {
                objectAux = this.cola.retirar();
                productoAux = (Productos) objectAux;
                if (productoAux.getCodigoProducto().equals(codigo)) {
                    if (cantidad <= productoAux.getCantidad() + productoAux.getMinimo()) {
                        if (cantidad <= productoAux.getCantidad()) {
                            productoAux.setCantidad(productoAux.getCantidad() - cantidad);
                        } else {
                            productoAux.setCantidad(0);
                            productoAux.setMinimo(productoAux.getMinimo() - (cantidad - productoAux.getCantidad()));
                        }

                        dato = "Venta realizada correctamente";
                    } else {
                        dato = "Venta no realizada, stock insuficiente";
                    }
                }
                colaAux.ingresar(productoAux);
            }
            this.cola = colaAux;
            this.txa_imprimir.setText(dato);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "La cola aun no se ha creado o la compra realizada es incorrecto");
        }
    }//GEN-LAST:event_btn_venderActionPerformed

    private void btn_examenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_examenActionPerformed
        try {
            Pila pila = new Pila(this.cola.getTamaño());
            Pila pilaAux = new Pila(this.cola.getTamaño());            
            Productos producto1;
            Productos producto2;
            int contador = 0;
            while (!this.cola.vacio()) {
                if (contador == 0) {
                    pila.apilar(this.cola.retirar());
                } else {
                    // la cola tiene el método primer elemento que devuelve un objeto, el primero de la cola
                    producto1 = (Productos) this.cola.primerElemento();
                    producto2 = (Productos) pila.cima();
                    if (producto2.getPrecio() > producto1.getPrecio()) {
                        pila.apilar(this.cola.retirar());
                    } else {
                        //mientras no el precio del elemento sea mayor seguira desapilando y colocandolos en la auxiliar
                        while (producto1.getPrecio() > producto2.getPrecio() && !pila.vacio()) {
                            pilaAux.apilar(pila.Desapilar());
                        }
                        //vuelve a apila los datos ordenados en la pila de la pila Aux
                        pila.apilar(this.cola.retirar());
                        while (!pilaAux.vacio()) {
                            pila.apilar(pilaAux.Desapilar());
                        }
                    }
                }
                contador++;
            }
            while (!pila.vacio()) {
                this.cola.ingresar(pila.Desapilar());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "La cola aun no se ha creado o tiene 0 elementos");
        }
    }//GEN-LAST:event_btn_examenActionPerformed

