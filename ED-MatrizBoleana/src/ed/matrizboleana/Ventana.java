    private void btn_juntaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_juntaActionPerformed
        // TODO add your handling code here:
        if (matriz == null) {
            JOptionPane.showMessageDialog(null, "llene los tres conjuntos por favor");
        } else {
            if ((matriz.getNum_col_A() == matriz.getNum_col_B() && matriz.getNum_fil_A() == matriz.getNum_fil_B())) {
                int r[][] = matriz.junta();
                dtmR = (DefaultTableModel) tb_resultante.getModel();
                dtmR.setColumnCount(r[0].length);
                dtmR.setRowCount(r.length);
                for (int i = 0; i < r.length; i++) {
                    for (int j = 0; j < r[0].length; j++) {
                        dtmR.setValueAt(r[i][j], i, j);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Para realizar la junta ambas matrices deben tener el mismo tamaño");
            }
        }
    }//GEN-LAST:event_btn_juntaActionPerformed

