    public boolean VALIDAR_TAMAÑO() {
        int num_1;
        int num_2;
        int num_3;
        int num_4;
        try {
            num_1 = Integer.parseInt(this.txt_col_A.getText());
            num_2 = Integer.parseInt(this.txt_col_B.getText());
            num_3 = Integer.parseInt(this.txt_fil_A.getText());
            num_4 = Integer.parseInt(this.txt_fil_B.getText());
            if ((num_1 > 0) && (num_2 > 0) && (num_3 > 0) && (num_4 > 0)) {
                return true;
            } else {
                return false;
            }
        } catch (Exception e) {
            return false;
        }
    }

    public boolean VALIDAR_NUMEROS() {
        int num;
        boolean permitir = true;
        int nfa = Integer.parseInt(this.txt_fil_A.getText());
        int nca = Integer.parseInt(this.txt_col_A.getText());
        int nfb = Integer.parseInt(this.txt_fil_B.getText());
        int ncb = Integer.parseInt(this.txt_col_B.getText());

        try {
            for (int i = 0; i < nfa; i++) {
                for (int j = 0; j < nca; j++) {
                    num = Integer.parseInt(this.tb_A.getValueAt(i, j) + "");
                    if (num < 0 || num > 1) {
                        permitir = false;
                        break;
                    }
                }
            }
        } catch (Exception e) {
            permitir = false;
        }

        if (permitir == false) {
            return permitir;
        } else {
            try {
                for (int i = 0; i < nfb; i++) {
                    for (int j = 0; j < ncb; j++) {
                        num = Integer.parseInt(this.tb_B.getValueAt(i, j) + "");
                        if (num < 0 || num > 1) {
                            permitir = false;
                            break;
                        }
                    }
                }
            } catch (Exception e) {
                permitir = false;
            }
            return permitir;
        }

    }

