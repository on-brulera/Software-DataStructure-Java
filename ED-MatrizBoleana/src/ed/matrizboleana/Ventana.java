    private void btn_llenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_llenarActionPerformed

        if (this.VALIDAR_TAMAÑO() == true) {
            if (this.VALIDAR_NUMEROS() == true) {
                int nfa = Integer.parseInt(this.txt_fil_A.getText());
                int nca = Integer.parseInt(this.txt_col_A.getText());
                int nfb = Integer.parseInt(this.txt_fil_B.getText());
                int ncb = Integer.parseInt(this.txt_col_B.getText());
                int mA[][] = new int[nfa][nca];
                int mB[][] = new int[nfb][ncb];
                for (int i = 0; i < nfa; i++) {
                    for (int j = 0; j < nca; j++) {
                        mA[i][j] = Integer.parseInt(dtmA.getValueAt(i, j) + "");
                    }
                }
                for (int i = 0; i < nfb; i++) {
                    for (int j = 0; j < ncb; j++) {
                        mB[i][j] = Integer.parseInt(dtmB.getValueAt(i, j) + "");
                    }
                }
                matriz = new Matriz_Booleana(mA, mB, nfa, nca, nfb, ncb);
            } else {
                JOptionPane.showMessageDialog(null, "los numeros de las matrices deben ser 1 o 0");
            }
        } else {
            JOptionPane.showMessageDialog(null, "llene el tamaño de las matrices correctamente y con numeros mayores a 0");
        }
    }//GEN-LAST:event_btn_llenarActionPerformed

